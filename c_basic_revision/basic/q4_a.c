#include<stdio.h>

int main()
{
    int n1,n2;
    printf("Enter n1 and n2:\n");
    scanf("%d %d",&n1,&n2);
    printf("\nAddition: %d",n1+n2);
    printf("\nSubstraction: %d",n1-n2);
    printf("\nMultiplication: %d",n1*n2);
    printf("\nDivision: %d",n1/n2);
    printf("\nRemainder: %d",n1%n2);

    return 0;
}
